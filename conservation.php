      <div class="col-md-8">
        <p class="lead">
          “Coral bleaching” occurs when coral polyps lose their symbiotic
          algae, the zooxanthellae. Without their zooxanthellae, the living
          tissues are nearly transparent, and you can see right through to
          the stony skeleton, which is white, hence the name coral
          bleaching. Many different kinds of stressors can cause coral
          bleaching – water that is too cold or too hot, too much or too
          little light, or the dilution of seawater by lots of fresh water
          can all cause coral bleaching.
          <br />
          The biggest cause of bleaching today has been rising temperatures
          caused by global warming. Temperatures more than 2 degrees F (or 1
          degree C) above the normal seasonal maximimum can cause bleaching.
          Bleached corals do not die right away, but if temperatures are
          very hot or are too warm for a long time, corals either die from
          starvation or disease. In 1998, 80 percent of the corals in the
          Indian Ocean bleached and 20 percent died.
